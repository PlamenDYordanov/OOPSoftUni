package Polymorphism.Lab.P01_MathOperation;

public class MathOperation {

    public int add(int i1, int i2) {
      return   i1 + i2;
    }

    public int add(int i1, int i2, int i3) {
        return i1 + i2 + i3;
    }

    public int add(int i1, int i2, int i3, int i4) {
        return i1+ i2 + i3 + i4;
    }
}
